#Operadores de comparacion nos devuelven True o False

igual_que = 5 == 4

distinto_de = 5 != 6

mayor_que = 5 > 6

menor_que = 5 < 6

mayor_o_igual = 5 >= 6    

menor_o_igual = 5 <= 5

print(igual_que)
print(distinto_de)
print(mayor_que)
print(menor_que)
print(mayor_o_igual)
print(menor_o_igual)

